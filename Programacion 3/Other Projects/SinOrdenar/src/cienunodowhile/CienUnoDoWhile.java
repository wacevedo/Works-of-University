public class CienUnoDoWhile {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
      int C = 100;
        System.out.println("Numeros del 100 al 1: ");
        do{
                   System.out.println(C);
                   C--;
        }
        while(C>=1);
    }
}
