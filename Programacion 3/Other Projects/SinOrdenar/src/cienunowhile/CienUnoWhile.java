public class CienUnoWhile {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       System.out.println("Numeros del 100 al 1: ");
         int C=100;
         while(C>=1) {
                   System.out.println(C);
                   C--;
         }
    }
}