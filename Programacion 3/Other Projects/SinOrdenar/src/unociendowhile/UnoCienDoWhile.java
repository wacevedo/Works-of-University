public class UnoCienDoWhile {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
         int C = 1;
        System.out.println("Numeros del 1 al 100: ");
        do{
                   System.out.println(C);
                   C++;
        }
        while(C<=100);
    }
}
